COMMENT ON COLUMN "TB_CLASS"."CLASS_NO" IS '과목 번호';
COMMENT ON COLUMN "TB_CLASS"."DEPARTMENT_NO" IS '학과 번호';
COMMENT ON COLUMN "TB_CLASS"."PREATTENDING_CLASS_NO" IS '선수 과목 번호';
COMMENT ON COLUMN "TB_CLASS"."CLASS_NAME" IS '과목 이름';
